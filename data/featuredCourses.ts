export function getFeaturedCoursesByType(type: FeaturedCourseType) {
  return featuredCourses.filter((course) => course.type === type);
}

export function getFeaturedCourseById(id: string) {
  return featuredCourses.find((course) => course.id === id) || null;
}

export function getFeaturedLessonById(id: string) {
  return featuredLessonRecords.find((course) => course.id === id) || null;
}

export function getSentencePromptForLanguage(
  sentence: FeaturedCourseSentence,
  language: AppLanguage
) {
  const translationKey = appLanguageToTranslationKey[language];
  if (!translationKey) {
    return sentence.english;
  }

  return sentence.translations[translationKey] || sentence.english;
}
